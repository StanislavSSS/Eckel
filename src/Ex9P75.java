public class Ex9P75 {
    public static void main (String[] args){
        Float f = new Float(2f);

        System.out.println("The max value of float is " + f.MAX_VALUE);
        System.out.println("The min value of float is " + f.MIN_VALUE);

        Double d = new Double(1d);
        System.out.println("The max value of double is " + d.MAX_VALUE);
        System.out.println("The min value of double is " + d.MIN_VALUE);
    }
}